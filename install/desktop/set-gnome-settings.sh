# fonts
gsettings set org.gnome.desktop.interface font-name 'Inter Regular 11'
gsettings set org.gnome.desktop.interface document-font-name 'Inter Regular 11'
gsettings set org.gnome.desktop.interface monospace-font-name 'Cascadia Mono PL Roman 13'

# show weekday on top bar
gsettings set org.gnome.desktop.interface clock-show-weekday true

# enable maximize button on pop os
gsettings set org.gnome.desktop.wm.preferences button-layout 'close,minimize,maximize:'

